#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	cnt;
	char	*ptr;

	ptr = (char *)s;
	cnt = 0;
	while (cnt < n)
	{
		ptr[cnt] = '\0';
		cnt++;
	}
}
